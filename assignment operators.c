/*asignment operators*/
#include<stdio.h>

int main()

{
    int b=20;
    printf("value of b is %d/n",b);
    b+=20;
    printf("value of b is %d/n",b);
    b-=20;
    printf("value of b is %d/n",b);
    b*=20;
    printf("value of b is %d/n",b);
    b/=20;
    printf("value of b is %d/n",b);
    return 0;
}
