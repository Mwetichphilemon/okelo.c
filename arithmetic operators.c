/*arithmetic operators*/
#include<stdio.h>
int main()
{
    int a=20,b=10,add,substract,mult,divide;
    add=a+b;
    substract=a-b;
    mult=a*b;
    divide=a/b;
    printf("results of addition is=%d\n", add);
    printf("results of substraction is=%d\n", substract);
    printf("results of multiplication is=%d\n", mult);
    printf("results of division is=%d\n", divide);
    return 0;
}
